/**
* The unit used to denote milliseconds
* @type {number}
*/
export const ms = 1;
/**
* The unit used to denote seconds
* @type {number}
*/
export const s = 1000 * ms;
/**
* The unit used to denote minutes
* @type {number}
*/
export const min = 60 * s;
/**
* The unit used to denote hours
* @type {number}
*/
export const h = 60 * min;
/**
* The unit used to denote days
* @type {number}
*/
export const d = 24 * h;
/**
* The unit used to denote meters
* @type {number}
*/
export const m = 1;
/**
* The unit used to denote kilometers
* @type {number}
*/
export const km = 1000 * m;